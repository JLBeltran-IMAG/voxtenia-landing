function updateLanguageButtons(activeLanguage) {
    const inactiveClasses = "rounded-full px-3 py-1.5 text-[11px] font-bold text-slate-400 transition hover:text-white";
    const activeClasses = "rounded-full bg-cyan-300 px-3 py-1.5 text-[11px] font-bold text-slate-950 transition";
    const buttonEs = document.getElementById("lang-es");
    const buttonEn = document.getElementById("lang-en");

    if (!buttonEs || !buttonEn) {
        return;
    }

    buttonEs.className = activeLanguage === "es" ? activeClasses : inactiveClasses;
    buttonEn.className = activeLanguage === "en" ? activeClasses : inactiveClasses;
}

function setLanguage(language) {
    const dictionary = translations[language];

    if (!dictionary) {
        return;
    }

    document.documentElement.lang = language;

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.getAttribute("data-i18n");

        if (dictionary[key]) {
            element.textContent = dictionary[key];
        }
    });

    updateLanguageButtons(language);
}

function setupLanguageSwitcher() {
    document.querySelectorAll("[data-language]").forEach((button) => {
        button.addEventListener("click", () => {
            setLanguage(button.dataset.language);
        });
    });
}

function setupScrollAnimations() {
    const animatedElements = document.querySelectorAll(".animate-on-scroll");

    if (!animatedElements.length) {
        return;
    }

    const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                entry.target.classList.add("is-visible");
                observer.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.12,
        rootMargin: "0px 0px -40px 0px"
    });

    animatedElements.forEach((element) => {
        observer.observe(element);
    });

    window.setTimeout(() => {
        animatedElements.forEach((element) => {
            const rect = element.getBoundingClientRect();

            if (rect.top < window.innerHeight - 20) {
                element.classList.add("is-visible");
            }
        });
    }, 120);
}

document.addEventListener("DOMContentLoaded", () => {
    setupLanguageSwitcher();
    setupScrollAnimations();
    setLanguage("es");
});
